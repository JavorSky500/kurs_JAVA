package Kalkulator;

public class Kalkulator {

    public double dodawanie (double a, double b){
        double wynik = a + b;
        return wynik;
    }
    public double odejmowanie (double a, double b) {
        double wynik = a - b;
        return wynik;
    }
    public double iloczyn (double a, double b) {
        double wynik = a * b;
        return wynik;
    }
    public double iloraz (double a, double b) {
        double wynik = a / b;
        return wynik;
    }


}
