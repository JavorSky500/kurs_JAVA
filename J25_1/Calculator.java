package J25_1;

public class Calculator {

    public int add(int a, int b) {
        int wynik = a + b;
        return wynik;
    }

    public int add(int a, int b, int c) {
        int wynik = a + b + c;
        return wynik;
    }

    public double add(double a, double b) {
        double wynik = a + b;
        return wynik;
    }

    public double add(double a, double b, double c) {
        double wynik = a + b + c;
        return wynik;
    }



}
